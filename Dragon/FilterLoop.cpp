//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
BOOL CFilterLoop::OnInitDialog()
{
	CDialogEx::OnInitDialog();
	CString str;
	m_ListBox.ResetContent();
	for( UINT i = 0; i < vLoops->size(); ++i )
	{
		str.Format( L"%d", vLoops->at(i) );
		 m_ListBox.AddString( str );
	}
	return TRUE;
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void CFilterLoop::OnDblclkList()
{
	int nItem;
	CString str;
	int index = m_ListBox.GetCurSel();
	m_ListBox.GetText( index, str );
	m_loop = _wtoi( str );
	CDialogEx::OnOK();
}
