/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
BOOL CFirstLetter::OnInitDialog()
{
	CDialogEx::OnInitDialog();
	GetDlgItem( IDC_EDIT )->SetWindowTextW( L"A" );

	return TRUE;
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void CFirstLetter::OnBnClickedOk()
{
	CString str;
	GetDlgItem( IDC_EDIT )->GetWindowTextW( str );
	if( str.IsEmpty() ) return;
	if( isNumeric( str ) ) return;
	str.MakeUpper();
	m_firstChar = str[0];
	CDialogEx::OnOK();
}
